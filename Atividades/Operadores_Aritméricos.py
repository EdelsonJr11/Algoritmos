print("Operadores Aritméticos")
print("Soma: 10 + 5 =", 10 + 5)
print("Subtração: 10 - 5 =", 10 - 5)
print("Multiplicação: 10 * 5 =", 10 * 5)
print("Divisão: 10 / 5 =", 10 / 5)
print("Divisão inteira(sem resto): 10 // 5 =", 10 // 5)
print("Módulo(resto da divisão): 10 % 5 =", 10 % 5)
print("Exponenciação: 10 ** 5 =", 10 ** 5)
print("Maior que: 10 > 5 =", 10 > 5)
print("Menor que: 10 < 5 =", 10 < 5)
print("Maior ou igual a: 10 >= 5 =", 10 >= 5)
print("Menor ou igual a: 5 <= 5 =", 5 <= 5)
print("Igual a: 10 == 5 =", 10 == 5)
print("Diferente de: 10 != 5 =", 10 != 5)
print("---Operadores com atribuição:---")
print("Esses fazem uma operação seguida de uma atribuição. Ou seja; o valor é atribuído à variável após a operação.")
print("10 += 5, equivale a 10 = 10 + 5, resultado:", (10 + 5))
print("10 -= 5, equivale a 10 = 10 - 5, resultado:", (10 - 5))
print("10 *= 5, equivale a 10 = 10 * 5, resultado:", (10 * 5))
print("10 /= 5, equivale a 10 = 10 / 5, resultado:", (10 / 5))
print("10 %= 5, equivale a 10 = 10 % 5, resultado:", (10 % 5))


